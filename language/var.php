    <h1>Language: Variables</h1>
    SmallCode is very flexible with variables, in fact it does not require prior initialization and they can be overwritten at any time.<br>
    It also doesn't enforce a variable's type, so once they've been defined they can safely change the type without further definition.<br><br>
    The type of the variables is imposed only when it is defined because according to the <a href='/docs/language/type'>type</a> it is saved in different ways.<br><br>
    <h3>Limitations</h3>
    Variables currently only support the following characters (plus " " experimentally): [a-z][A-Z][0-9][_-^$%&].
    <h3>Usage</h3>
    Variables are recognized by SmallCode as having no other signs such as ' for strings
