    <h1>Language: Types</h1>
    SmallCode does not aim to be a highly typed language but sometimes the definition of the type, even if only for archiving purposes is mandatory and it is really important.<br>
    At the moment there are only 3 types of variables:<br>
    <a href='/docs/language/string'><code><b>string</b></code></a>: A simple <a href='/docs/language/string'>string</a>. It can contain <a href='/docs/language/array'>arrays</a> or <a href='/docs/language/json'>json</a> as long as they meet the character requirements of the string.<br>
    <a href='/docs/language/array'><code><b>array</b></code></a>: An <a href='/docs/language/array'>array</a>.<br>
    <a href='/docs/language/json'><code><b>json</b></code></a>: A simple <a href='/docs/language/json'>json</a><br><br>
    The most used types are definitely string and array since the json is then transformed into an array.<br>
