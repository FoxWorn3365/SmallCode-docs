    <h1>Language: Variables</h1>
    At the expense of variables, SmallCode imposes many limitations on the definition and use of strings.
    <h3>Requirements</h3>
    Variables on SmallCode must always be defined with ' and never with a double apostrophe (") as they would not be recognized.
    <h3>Limits</h3>
    Variables can contain all characters except the apostrophe
    <h3>Examples</h3>
    Correct string: 'my name is federico'
    Wrong string: "my name is federico"
